package ABC;

public class Swappingofstrings {

	public static void main(String[] args) {
		String a="java";
		String b="language";
		System.out.println("before swap "+a);
		System.out.println("before swap "+b);
		String p=" ";
		p=a;
		a=b;
		b=p;
		System.out.println(a);
		System.out.println(b);
	}

}
